     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product = Product::whereSerial($request->get('serial'))->latest()->first();
        if($product){
            $reviews = ProductReview::where('product_id', $product->id)->get();
            return view('checkproduct.exists', ['product' => $product, 'reviews' => $reviews]);
        }else{
            $serial = $request->get('serial');
            return view('checkproduct.notfound', ['serial' => $serial]);
        }
    }

